package Modul_2;

public class Latihan_4 {

    public static void main(String[] args) {
        int a = 2;
        int b = 5;

        int penjumlahan = a + b;
        int pengurangan = a - b;
        int perkalian = a * b;
        int pembagian = a / b;
        int modulus = a % b;
        
        System.out.println("Penjumlahan = " + penjumlahan);
        System.out.println("Pengurangan = " + pengurangan);
        System.out.println("Perkalian = " + perkalian);
        System.out.println("Pembagian = " + pembagian);
        System.out.println("Modulus \t= " + modulus);
    }

}
